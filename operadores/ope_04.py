# Solicita ao usuário o primeiro número inteiro
numero1 = int(input("Digite o primeiro número inteiro: "))

# Solicita ao usuário o segundo número inteiro
numero2 = int(input("Digite o segundo número inteiro: "))

# Calcula o quociente inteiro da divisão
quociente = numero1 // numero2

# Calcula o resto da divisão
resto = numero1 % numero2

# Exibe os resultados
print(f"O quociente da divisão de {numero1} por {numero2} é {quociente}.")
print(f"O resto da divisão de {numero1} por {numero2} é {resto}.")


