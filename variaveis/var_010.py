# Solicita um número inteiro do usuário
numero = int(input("Digite um número inteiro: "))

# Armazena o número incrementado de 1 em outra variável
numero_incrementado = numero + 1

# Exibe os dois valores
print(f"O número informado foi: {numero}")
print(f"O número incrementado em 1 é: {numero_incrementado}")
